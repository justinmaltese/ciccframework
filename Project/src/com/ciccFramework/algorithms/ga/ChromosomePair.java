/* This class represents a pair of chromosomes.
 *  
 *  
 *  
 * @author Justin Maltese
 * @date 04/10/2016
 * @version 1.0
 */

public class ChromosomePair {
	public Chromosome first;
	public Chromosome second;
	public ChromosomePair(Chromosome a, Chromosome b) {
		first = a;
		second = b;
	}
}
